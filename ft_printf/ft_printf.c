int	print_number(int n)
{
	long int nbr;
	int	lenght;

	nbr = n;
	lenght = 0;
	if (nbr < 0)
	{
		lenght += print_char('-');
		nbr = -nbr;
	}
	if (nbr > 9)
		lenght += print_number(nbr / 10);
	lenght += print_char(nbr % 10);
	return (lenght);
}

